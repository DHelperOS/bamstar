--- public.handle_post_hashtags(post_id_in bigint, content_in text) RETURNS void
CREATE OR REPLACE FUNCTION public.handle_post_hashtags(post_id_in bigint, content_in text)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
-- ... (이전과 동일한 함수 내용) ...
DECLARE
  tag_name TEXT;
  tag_id_var INT;
  tags TEXT[];
BEGIN
  tags := ARRAY(SELECT DISTINCT lower(substring(unnest(regexp_matches(content_in, '#([^\s#]+)', 'g')) FOR 1)));
  FOREACH tag_name IN ARRAY tags
  LOOP
    INSERT INTO public.community_hashtags (name, last_used_at, post_count)
    VALUES (tag_name, now(), 1)
    ON CONFLICT (name) DO UPDATE
    SET last_used_at = now(), post_count = community_hashtags.post_count + 1;
    SELECT id INTO tag_id_var FROM public.community_hashtags WHERE name = tag_name;
    INSERT INTO public.post_hashtags (post_id, hashtag_id)
    VALUES (post_id_in, tag_id_var)
    ON CONFLICT DO NOTHING;
  END LOOP;
END;
$function$

