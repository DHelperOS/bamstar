--- public.get_top_posts_by_metric(window_days integer DEFAULT 7, metric text DEFAULT 'comments'::text, limit_val integer DEFAULT 20, offset_val integer DEFAULT 0) RETURNS TABLE(id bigint, author_id uuid, content text, is_anonymous boolean, image_urls text[], view_count bigint, created_at timestamp with time zone, likes_count bigint, comments_count bigint)
CREATE OR REPLACE FUNCTION public.get_top_posts_by_metric(window_days integer DEFAULT 7, metric text DEFAULT 'comments'::text, limit_val integer DEFAULT 20, offset_val integer DEFAULT 0)
 RETURNS TABLE(id bigint, author_id uuid, content text, is_anonymous boolean, image_urls text[], view_count bigint, created_at timestamp with time zone, likes_count bigint, comments_count bigint)
 LANGUAGE sql
 STABLE
AS $function$
WITH windowed_posts AS (
  SELECT p.*, p.id AS post_id
  FROM public.community_posts p
  WHERE (window_days IS NULL) OR (p.created_at >= now() - (window_days || ' days')::interval)
),
agg AS (
  SELECT wp.id AS id,
         wp.author_id,
         wp.content,
         wp.is_anonymous,
         wp.image_urls,
         wp.view_count,
         wp.created_at,
         COALESCE(l.likes_count, 0)::bigint AS likes_count,
         COALESCE(c.comments_count, 0)::bigint AS comments_count
  FROM windowed_posts wp
  LEFT JOIN (
    SELECT post_id, COUNT(*)::bigint AS likes_count
    FROM public.community_likes
    WHERE post_id IS NOT NULL
    GROUP BY post_id
  ) l ON l.post_id = wp.id
  LEFT JOIN (
    SELECT post_id, COUNT(*)::bigint AS comments_count
    FROM public.community_comments
    WHERE post_id IS NOT NULL
    GROUP BY post_id
  ) c ON c.post_id = wp.id
)
SELECT a.id, a.author_id, a.content, a.is_anonymous, a.image_urls, a.view_count, a.created_at,
       a.likes_count, a.comments_count
FROM agg a
ORDER BY
  CASE WHEN lower(metric) = 'likes' THEN a.likes_count
       WHEN lower(metric) = 'comments' THEN a.comments_count
       ELSE a.comments_count END DESC,
  a.created_at DESC
LIMIT GREATEST(1, COALESCE(limit_val, 20))
OFFSET GREATEST(0, COALESCE(offset_val, 0));
$function$

